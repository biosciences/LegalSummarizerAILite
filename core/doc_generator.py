import io
from docx import Document
from weasyprint import HTML
from markdown import markdown
import os

def summary_to_word(summary: str) -> io.BytesIO:
    doc = Document()
    for line in summary.splitlines():
        if line.startswith("##"):
            doc.add_heading(line.replace("##", "").strip(), level=1)
        elif line.startswith("###"):
            doc.add_heading(line.replace("###", "").strip(), level=2)
        else:
            doc.add_paragraph(line)
    doc.add_paragraph("\n---\nAI-generated summary. Not legal advice.")
    buffer = io.BytesIO()
    doc.save(buffer)
    buffer.seek(0)
    return buffer

def summary_to_pdf(summary: str) -> io.BytesIO:
    html = f"<html><body><pre>{summary}</pre><hr><em>AI-generated summary. Not legal advice.</em></body></html>"
    buffer = io.BytesIO(HTML(string=html).write_pdf())
    buffer.seek(0)
    return buffer

